        public override string[] GetRolesForUser(string username)
        {
            using (SystemDbContext _Context = new SystemDbContext())
            {
                var userRoles = (from admin in _Context.Admins
                                 join roleMapping in _Context.UserRoleMappings
                                 on admin.Id equals roleMapping.AdminId
                                 join role in _Context.Roles
                                 on roleMapping.RolesId equals role.Id
                                 where admin.UserName == username
                                 select role.RoleName).ToArray();
                return userRoles;
            }
        }

